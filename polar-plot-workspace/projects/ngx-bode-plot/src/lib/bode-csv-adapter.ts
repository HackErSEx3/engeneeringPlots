// Adapter for the desktop Bode CSV export format.
//
// Source file: bode_plotData.csv at the repo root.
// Header block (7 rows) is metadata; row 8 is the CSV column header; data starts at row 9.
// Columns: X-Axis Value, Y-Axis Value, Y-Axis Status, Phase, Phase Status, Timestamp

import type {
  BodeAxisState,
  BodeCompositeCurve,
  BodeCurve,
  StatusPoint,
} from './bode-plot.models';

// ── Types ─────────────────────────────────────────────────────────────────────

/** Compact per-row tuple: [rpm, amp, ampStatus, phaseDeg, phaseStatus, timestampStr] */
export type BodeSampleTuple = [number, number, string, number, string, string];

export interface ParsedBode {
  composite: BodeCompositeCurve;
  axis: Partial<BodeAxisState>;
  metadata: Record<string, string>;
}

// ── Helpers ───────────────────────────────────────────────────────────────────

const STATUS_MAP: Record<string, StatusPoint> = {
  Valid: 'Valid',
  Invalid: 'Invalid',
  Overrange: 'Overrange',
  Underrange: 'Underrange',
};

function toStatus(raw: string): StatusPoint {
  const key = (raw ?? '').trim();
  return STATUS_MAP[key] ?? 'Invalid';
}

function toEpochMs(raw: string): number | undefined {
  if (!raw) return undefined;
  const parsed = Date.parse(raw);
  return Number.isFinite(parsed) ? parsed : undefined;
}

// ── Parser ────────────────────────────────────────────────────────────────────

/**
 * Parses the raw CSV text produced by the desktop Bode export.
 * Metadata block ends at the first row whose first cell is "X-Axis Value".
 */
export function parseBodeCsv(csvText: string): ParsedBode {
  const lines = csvText.split(/\r?\n/);
  const metadata: Record<string, string> = {};

  let dataStart = -1;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line) continue;
    // Strip UTF-8 BOM on first line
    const clean = i === 0 ? line.replace(/^﻿/, '') : line;
    const commaIdx = clean.indexOf(',');
    if (commaIdx === -1) continue;
    const key = clean.slice(0, commaIdx);
    const value = clean.slice(commaIdx + 1);
    if (key === 'X-Axis Value') { dataStart = i + 1; break; }
    metadata[key] = value;
  }

  if (dataStart === -1) {
    throw new Error('parseBodeCsv: header row "X-Axis Value" not found');
  }

  const xValues: number[] = [];
  const ampValues: number[] = [];
  const ampStatus: StatusPoint[] = [];
  const phaseValues: number[] = [];
  const phaseStatus: StatusPoint[] = [];
  const timestamps: number[] = [];
  let anyTimestamp = false;
  let anyPhase = false;

  for (let i = dataStart; i < lines.length; i++) {
    const line = lines[i]?.trim();
    if (!line) continue;
    const parts = line.split(',');
    if (parts.length < 3) continue;
    const x = Number(parts[0]);
    const y = Number(parts[1]);
    if (!Number.isFinite(x) || !Number.isFinite(y)) continue;
    xValues.push(x);
    ampValues.push(y);
    ampStatus.push(toStatus(parts[2] ?? ''));
    const p = Number(parts[3]);
    if (Number.isFinite(p)) { phaseValues.push(p); anyPhase = true; } else { phaseValues.push(NaN); }
    phaseStatus.push(toStatus(parts[4] ?? ''));
    const ts = toEpochMs(parts[5] ?? '');
    if (ts !== undefined) { timestamps.push(ts); anyTimestamp = true; } else { timestamps.push(NaN); }
  }

  const label = metadata['Point Name'] ?? metadata['Machine Name'] ?? 'Bode';
  const ampUnit = metadata['Y-Axis Unit'] ?? '';

  const amplitude: BodeCurve = {
    curveNumber: 1,
    xValues,
    yValues: ampValues,
    yStatus: ampStatus,
    timestampsUtc: anyTimestamp ? timestamps : undefined,
    style: { color: '#378ADD', lineWidth: 1.5, activeOpacity: 1, inactiveOpacity: 0.35 },
  };

  const phase: BodeCurve | undefined = anyPhase ? {
    curveNumber: 2,
    xValues,
    yValues: phaseValues,
    yStatus: phaseStatus,
    timestampsUtc: anyTimestamp ? timestamps : undefined,
    style: { color: '#378ADD', lineWidth: 1.5, activeOpacity: 1, inactiveOpacity: 0.35 },
  } : undefined;

  const composite: BodeCompositeCurve = {
    primaryCurveNumber: 1,
    amplitude,
    phase,
    curveType: 'primary',
    isActive: true,
    label,
  };

  const xMin = xValues.length ? Math.min(...xValues) : 0;
  const xMax = xValues.length ? Math.max(...xValues) : 1;
  const ampMax = ampValues.length ? Math.max(...ampValues) : 1;

  const axis: Partial<BodeAxisState> = {
    xRange: [xMin, xMax],
    yAmpRange: [0, ampMax],
    yPhaseRange: [0, 360],
    xUnit: 'RPM',
    yAmpUnit: ampUnit,
    phaseRolloverEnabled: false,
  };

  return { composite, axis, metadata };
}

