/**
 * Builds a demo composite curve + axis state directly from the embedded tuple array.
 * Skips the CSV text-parsing step entirely for a faster demo bootstrap.
 */
export function buildSampleComposite(): ParsedBode {
  const xValues: number[] = [];
  const ampValues: number[] = [];
  const ampStatus: StatusPoint[] = [];
  const phaseValues: number[] = [];
  const phaseStatus: StatusPoint[] = [];
  const timestamps: number[] = [];

  for (const [x, y, ys, p, ps, ts] of bodeSampleTuples) {
    xValues.push(x);
    ampValues.push(y);
    ampStatus.push(toStatus(ys));
    phaseValues.push(p);
    phaseStatus.push(toStatus(ps));
    const parsed = Date.parse(ts);
    timestamps.push(Number.isFinite(parsed) ? parsed : NaN);
  }

  const amplitude: BodeCurve = {
    curveNumber: 1,
    xValues,
    yValues: ampValues,
    yStatus: ampStatus,
    timestampsUtc: timestamps,
    style: { color: '#378ADD', lineWidth: 1.5 },
  };

  const phase: BodeCurve = {
    curveNumber: 2,
    xValues,
    yValues: phaseValues,
    yStatus: phaseStatus,
    timestampsUtc: timestamps,
    style: { color: '#378ADD', lineWidth: 1.5 },
  };

  const composite: BodeCompositeCurve = {
    primaryCurveNumber: 1,
    amplitude,
    phase,
    curveType: 'primary',
    isActive: true,
    label: 'STG-10 · VX-1X (VERTICAL) · 1X',
  };

  const axis: Partial<BodeAxisState> = {
    xRange: [Math.min(...xValues), Math.max(...xValues)],
    yAmpRange: [0, Math.max(...ampValues)],
    yPhaseRange: [0, 360],
    xUnit: 'RPM',
    yAmpUnit: 'mil pp',
    phaseRolloverEnabled: false,
  };

  return { composite, axis, metadata: {
    'Machine Name': 'STG-10',
    'Point Name': 'VX-1X (VERTICAL)',
    'X-Axis Unit': 'rpm',
    'Y-Axis Unit': 'mil pp',
    'Variable': '1X',
  } };
}

